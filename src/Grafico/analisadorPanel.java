    private void ajustarLarguraColunas() {
        // Obtenha a largura da tabela
        int larguraTabela = jTable1.getWidth();

        // Número de colunas na tabela
        int numColunas = jTable1.getColumnCount();

        // Calcule a largura aproximada para cada coluna
        int larguraColuna = larguraTabela / numColunas;

        // Ajuste a largura de cada coluna
        for (int i = 0; i < numColunas; i++) {
            jTable1.getColumnModel().getColumn(i).setPreferredWidth(larguraColuna);
        }
    }
    
