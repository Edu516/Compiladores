    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String textoDigitado = jTextArea1.getText();
        
        Automato automatoIf = new Automato();
        AnalisadorSintatico analisador = new AnalisadorSintatico(automatoIf);
        analisador.validarEntrada(textoDigitado);
        adicionarTokensNaTabela(analisador.getTokens());
    }//GEN-LAST:event_jButton1ActionPerformed

    private void adicionarTokensNaTabela(List<token> listaDeTokens) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        model.setRowCount(0);
        
        for (token t : listaDeTokens) {
            Object[] rowData = {
                    t.getCod(),
                    t.getLexema(),
                    t.getTipo(),
                    t.getPosInicial(),
                    t.getPosFinal()
            };
            model.addRow(rowData);
        }
    }
    
